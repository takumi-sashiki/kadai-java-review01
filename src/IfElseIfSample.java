
public class IfElseIfSample {

    public static void main(String[] args) {
        int score = 80;

        if (score >= 100) {
            System.out.println("mannten");
        } else if (score >= 80) {
            System.out.println("gannbattane");
        } else if (score >= 60) {
            System.out.println("goukaku");
        } else
            System.out.println("akaten");
    }
}
